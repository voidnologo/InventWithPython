print("Please enter a number: ")
number1 = int(input())

print("Enter another number: ")
number2 = int(input())

print("Enter another number: ")
number3 = int(input())

add = number1 + number2 + number3
print(str(number1) + " + " + str(number2) + " + " + str(number3) + " = " + str(add))

subtract = number1 - number2 - number3
print(str(number1) + " - " + str(number2) + " - " + str(number3) + " = " + str(subtract))

multiply = number1 * number2 * number3
print(str(number1) + " * " + str(number2) + " * " + str(number3) + " = " + str(multiply))
